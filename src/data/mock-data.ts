// ─── Helper functions ────────────────────────────

export function getArticlesForRun(runId: string): ArticleWithScore[] {
  const articleIds = RUN_ARTICLE_MAP[runId] ?? [];
  return MOCK_ARTICLES_WITH_SCORES.filter((a) => articleIds.includes(a.article.id));
}

export function getQueueArticles(): ArticleWithScore[] {
  return MOCK_ARTICLES_WITH_SCORES.filter(
    (a) => a.scored.status === 'new' && !a.scored.is_duplicate && !a.scored.drop_reason
  );
}

export function getReviewedArticles(): ArticleWithScore[] {
  return MOCK_ARTICLES_WITH_SCORES.filter((a) => a.scored.status === 'reviewed');
}

export function getDroppedArticles(): ArticleWithScore[] {
  return MOCK_ARTICLES_WITH_SCORES.filter(
    (a) => a.scored.status === 'dismissed' || a.scored.is_duplicate || a.scored.drop_reason !== null
  );
}
